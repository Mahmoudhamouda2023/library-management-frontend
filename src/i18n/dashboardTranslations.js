export function useDashboardText() {
  const languageContext = useLanguage();
  const { language, t } = languageContext;

  const dt = (key) => dashboardTranslations[language]?.[key] || dashboardTranslations.ar[key] || t(key) || key;

  return { ...languageContext, dt };
}

